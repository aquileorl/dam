
//OPERADORES ARITMETICOS
let x = 5; 

//SUMA, RESTA, MULTIPLICACIÓN, DIVISIÓN Y ASINGNACIÓN
//x = x + 5;
x += 5;
console.log(x);

//x = x -3
x -=3;
console.log(x);

//x = x*3;
x *= 3;
console.log(x);

// x = x/2;
x /=2;
console.log(x);

//INCREMENTO Y DECREMENTO
x++; //para sumar 1
console.log("Incremento, es decir, sumando 1: ", x);

x--; //para restar 1
console.log(x);

//RESTO
let y = 10%3;
console.log(y);


